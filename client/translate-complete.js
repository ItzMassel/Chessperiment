// Function to translate text
function translateDE2EN(text) {
    if (typeof text !== 'string') return text;
    if (translations[text]) return translations[text];
    // If not found, return original text (for missing translations)
    return text;
}

// Recursive function to translate object
function translateObject(obj) {
    if (typeof obj === 'string') {
        return translateDE2EN(obj);
    } else if (Array.isArray(obj)) {
        return obj.map(translateObject);
    } else if (typeof obj === 'object' && obj !== null) {
        const result = {};
        for (const key in obj) {
            result[key] = translateObject(obj[key]);
        }
        return result;
    }
    return obj;
}

